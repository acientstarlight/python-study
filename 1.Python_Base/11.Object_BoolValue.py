# 对象的布尔值
# 以下对象的布尔值为Flase
print(bool(False),bool(),bool(None))
print(bool(0),bool(0.0))
print(bool(""),bool(''))
print(bool(list()))
print(bool([]))
print(bool(set()))
print(bool({}))
print(bool(dict()))
print(bool())
print(bool(tuple()))
# 以下对象的布尔值为True
print(bool(True),bool(18),bool("hello"),bool(19),bool(23.56),bool([1,2,3]),bool((1,2,3)),bool({1:2,3:4}))


