# 嵌套循环中break语句
"""
1-5分别和100-199相乘，输出每次乘积中的第一个水仙花数
当乘积组成水仙花数时跳过
"""
for i in range(1,6):
    for j in range(100,200):
        if (i*j//100)**3+(i*j//10%10)**3+(i*j%10)**3==i*j:
            print("找到了水仙花数，内循环执行到了第", j-99, "次","构成第一个水仙花数乘积的两个数为",i," ",j)
            break
    print("外循环执行了第",i,"次")


